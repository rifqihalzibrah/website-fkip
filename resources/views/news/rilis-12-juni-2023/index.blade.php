              <h2 class="display-5 link-body-emphasis mb-1">GELAR WORKSHOP KEBAHASAAN, FKIP UNSA MAKASSAR 
                BERUPAYA TINGKATKAN KEMAMPUAN JURNALISTIK DAN KEPENYIARAN 
                DI ERA DIGITAL </h2>
              <p class="blog-post-meta">12 Juni, 2023</p>
      
              <p>Makassar FKIP PERS UNSA. Merespon perkembangan Era Digital, FKIP UNSA gelar Workshop Kebahasaan bertajuk “Workshop Peningkatan Kompetensi Kebahasaan di Era Digital.”</p>

              <p>Dunia jurnalistik dan kepenyiaran adalah salah satu bidang kerja yang membutuhkan kompetensi kebahasaan. Bidang kerja ini bukan hanya diperuntukkan untuk mahasiswa jurusan Bahasa saja namun semua mahasiswa berpeluang untuk menjadikannya sebagai lapangan kerja masa depan. </p>

              <p>Disela-sela kegiatan Workshop, Dekan FKIP UNSA Makassar, Nana Harlina Haruna, S.Pd., M.Pd. menyatakan bahwa “Kegiatan ini diselenggarakan dalam rangka Pengabdian Masyarakat (PKM) FKIP UNSA Makassar. FKIP UNSA memberikan ruang berlatih bagi Mahasiswa Universitas Sawerigading Makassar maupun pihak umum untuk bersama-sama belajar dalam kegiatan Workshop kebahasaan terutama dalam bidang jurnalistik dan kepenyiaran. Harapan saya kegiatan ini betul-betul dapat memberi manfaat bagi peserta maupun bagi kami selaku pelaksana kegiatan.” Ujarnya</p>

              <p>Kegiatan ini terselenggara  pada 10-12 Juni 2023 di Aula Universtas Sawerigading Makassar diikuti oleh Mahasiswa UNSA, dengan menghadirkan Trainer muda dan berbakat berasal dari salah satu kantor berita di Makassar Tribun Timur, Ibu Nurfajriani R, S.S., M.Pd. yang sehari-hari bekerja sebagai Jurnalis dan Voice Over media Tribun Timur, dan Penyiar andalan dari Radio Republik Indonesia, Dinindia Dwizhana, S.I.P.</p>

              <p>“Wokshop yang betul-betul lengkap karena menghadirkan trainer bukan hanya dari media cetak tapi juga dari radio, sehingga materinya komplit dengan aplikasi langsung di lapangan, semoga kegiatan ini sering-sering diadakan” ujar Astuti Peserta Workshop. </p>

              <p>Workshop ini ditutup dengan pemberian hadiah bagi peserta workshop yang berhasil menuntaskan tugas selama kegiatan. </p>
